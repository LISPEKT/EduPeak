    case 'Статистика и вероятность':
      return Color(0xFF00BCD4);
    case 'Физика':
      return Color(0xFF9C27B0);
    case 'Химия':
      return Color(0xFFFF9800);
    case 'Биология':
      return Color(0xFF4CAF50);
    case 'География':
      return Color(0xFF00BCD4);
    case 'История':
      return Color(0xFF34A853);
    case 'Обществознание':
